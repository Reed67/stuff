// Zuweisung
var a = 2;
print('Zuweisung: a ist ' + a);

// einfache Addition 
a += 2;
print('einfache Addition: a ist ' + a);
// Addition mit mehreren Variablen
var b = 4;
var c = 9;
a = b + c;
print('Addition mit mehreren Variablen: a ist ' + a);
// Inkrement
a++;
print('Inkrement: a ist ' + a);

// einfache Subtraktion 
a -= 7;
print('einfache Subtraktion: a ist ' + a);
// Subtraktion mit mehreren Variablen
var b = 4;
var c = 9;
a = c - b;
print('Subtraktion mit mehreren Variablen: a ist ' + a);
// Dekrement
a--;
print('Dekrement: a ist ' + a);


